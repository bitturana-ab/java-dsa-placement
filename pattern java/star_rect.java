class star_rect{
    public static void main(String args[]){
        int n=4;
        int m=5;
        for(int i=0;i<n;i++){
            //outer loop
            for(int j=0;j<m;j++){
                //inner loop
                System.out.print("*");
            }
            System.out.print("\n");
            //System.out.println();
        }
    }
}